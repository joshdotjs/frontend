import { useEffect, useState } from 'react';

// ==============================================
// ==============================================
// ==============================================
// ==============================================

function useTimer() {
  const [seconds, setSeconds] = useState(0);

  // ============================================

  useEffect(() => {

    const timeout = setTimeout(() => {
      setSeconds(prev => prev + 1);
    }, 1e3);

    return () => clearTimeout(timeout);
  }, [seconds]);

  // ============================================

  function start(s) {
    setSeconds(s);
  }

  // ============================================

  

  // ============================================

  return { seconds, start };
}

// ==============================================
// ==============================================
// ==============================================
// ==============================================

export { useTimer };